  name: 'app',
  data: function() {
    return {
      landscapes: [],
      landscapesUrl: 'api/v1/zabbix/landscapes'
    }
  },
  methods: {
    getLandscapes: function() {
      var vm = this;
      axios.get(this.landscapesUrl).then(function(response) {
        vm.landscapes = response.data.status != 'error' ? response.data.message : [];
      }).catch(function(err) {
        console.error(err);
      });
    }
  },
  mounted: function() {
      console.log("now get landscape");
      this.getLandscapes();
  }
}
